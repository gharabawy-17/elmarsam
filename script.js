document.addEventListener('DOMContentLoaded', () => {
    // ---- Navbar Scroll Effect ----
    const navbar = document.querySelector('.navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // ---- Theme Switching Logic ----
    const themeToggle = document.getElementById('theme-toggle');
    const mobileThemeToggle = document.getElementById('mobile-theme-toggle');
    
    // Check for saved theme
    const savedTheme = localStorage.getItem('theme');
    if (savedTheme) {
        document.documentElement.setAttribute('data-theme', savedTheme);
        updateThemeIcon(savedTheme);
    }
    
    function toggleTheme() {
        const currentTheme = document.documentElement.getAttribute('data-theme');
        const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
        
        document.documentElement.setAttribute('data-theme', newTheme);
        localStorage.setItem('theme', newTheme);
        updateThemeIcon(newTheme);
    }
    
    function updateThemeIcon(theme) {
        const iconClasses = theme === 'dark' ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
        if (themeToggle) themeToggle.querySelector('i').className = iconClasses;
        if (mobileThemeToggle) mobileThemeToggle.querySelector('i').className = iconClasses;
    }
    
    if (themeToggle) themeToggle.addEventListener('click', toggleTheme);
    if (mobileThemeToggle) mobileThemeToggle.addEventListener('click', toggleTheme);

    // Initial state check
    const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
    updateThemeIcon(isDark ? 'dark' : 'light');

    // ---- Mobile Menu Toggle ----
    const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
    const closeMenuBtn = document.querySelector('.close-menu-btn');
    const mobileMenu = document.querySelector('.mobile-menu');
    const overlay = document.querySelector('.mobile-menu-overlay');
    const mobileLinks = document.querySelectorAll('.mobile-link');

    function toggleMenu() {
        mobileMenu.classList.toggle('active');
        overlay.classList.toggle('active');
        document.body.style.overflow = mobileMenu.classList.contains('active') ? 'hidden' : '';
    }

    mobileMenuBtn.addEventListener('click', toggleMenu);
    closeMenuBtn.addEventListener('click', toggleMenu);
    overlay.addEventListener('click', toggleMenu);
    
    mobileLinks.forEach(link => {
        link.addEventListener('click', toggleMenu);
    });

    // ---- AOS Animation Initialization ----
    AOS.init({
        duration: 800,
        easing: 'ease-out-cubic',
        once: true,
        mirror: false,
        offset: 80
    });

    // ---- Swiper Slider Initialization ----
    const testimonialsSwiper = new Swiper('.testimonials-slider', {
        slidesPerView: 1,
        spaceBetween: 30,
        loop: true,
        autoplay: {
            delay: 5000,
            disableOnInteraction: false,
        },
        pagination: {
            el: '.swiper-pagination',
            clickable: true,
        },
        breakpoints: {
            768: {
                slidesPerView: 2,
            },
            1024: {
                slidesPerView: 2,
            }
        }
    });

    // ---- Gallery Image Modal ----
    const galleryItems = document.querySelectorAll('.gallery-item');
    const modal = document.getElementById('image-modal');
    const modalImg = document.getElementById('modal-img');
    const closeBtn = document.querySelector('.modal-close');

    galleryItems.forEach(item => {
        item.addEventListener('click', () => {
             const img = item.querySelector('img');
             modalImg.src = img.src;
             modal.classList.add('active');
             document.body.style.overflow = 'hidden';
        });
    });

    closeBtn.addEventListener('click', closeModal);
    modal.addEventListener('click', (e) => {
        if (e.target === modal) {
            closeModal();
        }
    });

    function closeModal() {
        modal.classList.remove('active');
        document.body.style.overflow = '';
    }

    // ---- Language Switcher (i18n) ----
    const langBtn = document.getElementById('lang-switcher');
    const currentLangSpan = document.getElementById('current-lang');
    let currentLang = 'ar';

    function setLanguage(lang) {
        currentLang = lang;
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        currentLangSpan.textContent = lang === 'ar' ? 'English' : 'عربي';

        // Update Text
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                 if (el.tagName.toLowerCase() === 'input' && el.type === 'text' || el.tagName.toLowerCase() === 'input' && el.type === 'email' || el.tagName.toLowerCase() === 'textarea') {
                      el.placeholder = translations[lang][key]
                 } else {
                      // Apply innerHTML for titles to support span tags
                      if (el.classList.contains('section-title') || el.classList.contains('hero-title')) {
                          el.innerHTML = translations[lang][key];
                      } else {
                          el.textContent = translations[lang][key];
                      }
                 }
            }
        });
         
        // Specific fix for Hero Title HTML structure
         const heroPrefix = document.querySelector('.highlight[data-i18n="heroTitlePrefix"]');
         const heroBrand = document.querySelector('.brand-name[data-i18n="heroBrand"]');
         
         if(heroPrefix && heroBrand) {
             heroPrefix.textContent = translations[lang]['heroTitlePrefix'];
             heroBrand.textContent = translations[lang]['heroBrand'];
         }

        // Refresh AOS to recalculate positions
        setTimeout(() => {
            AOS.refresh();
        }, 100);
    }

    langBtn.addEventListener('click', () => {
        const newLang = currentLang === 'ar' ? 'en' : 'ar';
        setLanguage(newLang);
    });

    // Initialize with Arabic
    setLanguage('ar');
    
    // Smooth scrolling for anchor links 
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if(targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if(targetElement) {
                const offset = 80; // Approximate navbar height
                const bodyRect = document.body.getBoundingClientRect().top;
                const targetRect = targetElement.getBoundingClientRect().top;
                const targetPosition = targetRect - bodyRect - offset;

                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });
});
